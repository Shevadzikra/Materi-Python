# ELIF else if statement

x = input("Kamu pengen dia balik? \n")

if x == "gak tau":
    print("percaya deh, dia ga mau balikan ama lu")
elif x == "mau":
    print("dia ga bakal mau.")
elif x == "gamau":
    print("yaudah sih, dia aja gamau kok.")
else:
    print("SAMPE KAPANPUN DIA GABAKAL MAU BALIKAN SAMA LU.")
